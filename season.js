let month = 1;

if (month === 2 || month === 3) {
  console.log("Spring");
}

else if (month === 4 || month === 5) {
  console.log("Summer");
}

else if (month === 6 || month === 7 || month === 8) {
  console.log("Monsoon");
}

else if (month === 9 || month === 10) {
  console.log("Autumn");
}

else if (month === 11) {
  console.log("Late Autumn / Hemanta");
}

else if (month === 12 || month === 1) {
  console.log("Winter");
}

else {
  console.log("Enter Valid Month");
}